    private void txtMontoCKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMontoCKeyTyped
        if (!Character.isDigit(evt.getKeyChar()) && evt.getKeyChar() != '.') {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "ingresar solo numeros");
        }
        if (evt.getKeyChar() == '.' && txtMontoC.getText().contains(".")) {
            getToolkit().beep();
            evt.consume();
            JOptionPane.showMessageDialog(rootPane, "ya hay un punto decimal");
        }
    }//GEN-LAST:event_txtMontoCKeyTyped

